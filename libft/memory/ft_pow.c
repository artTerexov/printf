#include <stddef.h>
long long 	ft_pow(long long a, size_t n) 
{
	if (n == 0)
		return 1;
	if (n == 1)
		return a;
	return n % 2 == 0 ? ft_pow(a * a, n / 2) : a * ft_pow(a, n - 1);
}
